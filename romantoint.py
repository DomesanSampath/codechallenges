#Roman Input to Integer Values 
x=input("Enter the Roman Value \n")
temp=0
for y in range(0,len(x)):
    if x[y]=='V':
        temp=temp+5
    if x[y]=='I':
        temp=temp+1
    if x[y]=='X':
        temp=temp+10
    if x[y]=='L':
        temp=temp+50
    if x[y]=='C':
        temp=temp+100
    if x[y]=='D':
        temp=temp+500
    if x[y]=='D':
        temp=temp+1000   
print(temp)
